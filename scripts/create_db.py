def main():
    out = os.path.abspath(OUT_PATH)
    os.makedirs(os.path.dirname(out), exist_ok=True)

    if os.path.exists(out):
        os.remove(out)

    con = sqlite3.connect(out)
    con.executescript(SCHEMA)

    for rec in RECIPES:
        name, url, cuisine, main_ing, method, kcal, serving, ings = rec
        rid = str(uuid.uuid4())
        con.execute(
            "INSERT INTO recipes VALUES (?,?,?,?,?,?,?,?)",
            (rid, name, url, cuisine, main_ing, method, float(kcal), serving),
        )
        for ing_name, amount, unit, category in ings:
            iid = str(uuid.uuid4())
            con.execute(
                "INSERT INTO ingredients VALUES (?,?,?,?,?,?)",
                (iid, rid, ing_name, float(amount), unit, category),
            )

    con.commit()
    con.close()
    print(f"Created {out}  ({len(RECIPES)} recipes)")


if __name__ == "__main__":
    main()
